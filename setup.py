def setup_style(
    font_size: int = 10,
    fig_width_inches: float = 3.5,
    aspect_ratio: float = 0.75,
    use_tex: bool = False,
    dpi: int = 300,
):
    """
    Configure matplotlib for journal-quality figures.

    Parameters
    ----------
    font_size : int
        Base font size (default 10 pt, standard for most journals).
    fig_width_inches : float
        Figure width. Common values:
        - 3.5  = single column
        - 5.5  = 1.5 column
        - 7.0  = full page width
    aspect_ratio : float
        Height / width ratio (default 0.75).
    use_tex : bool
        If True, use LaTeX rendering (requires texlive).
    dpi : int
        Resolution for raster output.
    """
    if not HAS_MPL:
        return

    fig_height = fig_width_inches * aspect_ratio

    plt.rcParams.update({
        # Font
        "font.family": "serif" if use_tex else "DejaVu Serif",
        "font.size": font_size,
        "text.usetex": use_tex,

        # Figure
        "figure.figsize": (fig_width_inches, fig_height),
        "figure.dpi": dpi,
        "savefig.dpi": dpi,
        "savefig.bbox": "tight",
        "savefig.pad_inches": 0.02,

        # Axes
        "axes.labelsize": font_size,
        "axes.titlesize": font_size + 1,
        "axes.linewidth": 0.6,
        "axes.grid": True,
        "axes.grid.which": "major",
        "axes.spines.top": False,
        "axes.spines.right": False,

        # Grid
        "grid.alpha": 0.3,
        "grid.linewidth": 0.4,

        # Ticks
        "xtick.labelsize": font_size - 1,
        "ytick.labelsize": font_size - 1,
        "xtick.direction": "in",
        "ytick.direction": "in",
        "xtick.major.width": 0.5,
        "ytick.major.width": 0.5,
        "xtick.minor.visible": True,
        "ytick.minor.visible": True,

        # Lines
        "lines.linewidth": 1.2,
        "lines.markersize": 4,

        # Legend
        "legend.fontsize": font_size - 1,
        "legend.frameon": False,
        "legend.handlelength": 1.5,
    })


def save_figure(
    fig,
    name: str,
    output_dir: str | Path | None = None,
    formats: tuple[str, ...] = ("pdf", "png"),
    dpi: int = 300,
    transparent: bool = False,
):
    """
    Save a figure in multiple formats for paper inclusion.

    Parameters
    ----------
    fig : matplotlib.figure.Figure
        The figure to save.
    name : str
        Base filename (without extension).
    output_dir : str or Path, optional
        Directory to save into. Defaults to paper/figures/.
    formats : tuple of str
        Output formats (default: PDF for LaTeX, PNG for preview).
    dpi : int
        Resolution for raster formats.
    transparent : bool
        Transparent background.
    """
    if output_dir is None:
        # Default: paper/figures/ relative to repo root
        output_dir = Path(__file__).resolve().parent.parent.parent / "paper" / "figures"

    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    for fmt in formats:
        path = output_dir / f"{name}.{fmt}"
        fig.savefig(
            path,
            format=fmt,
            dpi=dpi,
            bbox_inches="tight",
            pad_inches=0.02,
            transparent=transparent,
        )
    plt.close(fig)


