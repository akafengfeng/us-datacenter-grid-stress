def ask(prompt: str, default: str = "") -> str:
    """Prompt the user interactively; return default if empty."""
    suffix = f" [{default}]" if default else ""
    answer = input(f"{prompt}{suffix}: ").strip()
    return answer if answer else default


def seed_file(path: Path, content: str, overwrite: bool = False):
    """Write content to path, creating parent dirs. Skip if exists unless overwrite."""
    path.parent.mkdir(parents=True, exist_ok=True)
    if path.exists() and not overwrite:
        print(f"  [skip] {path.relative_to(ROOT)} (already exists)")
        return
    path.write_text(content, encoding="utf-8")
    print(f"  [seed] {path.relative_to(ROOT)}")


def run_agent(role: str, prompt: str, timeout_min: int, model: str = "claude-opus-4-6"):
    """
    Run a Claude agent via the CLI.

    Returns (success: bool, elapsed_sec: float).
    """
    cmd = [
        "claude",
        "--dangerously-skip-permissions",
        "--output-format", "stream-json",
        "--verbose",
        "--max-turns", "50",
        "--model", model,
        "-p", prompt,
    ]

    print(f"\n{'='*60}")
    print(f"  Running {role} agent (timeout: {timeout_min} min, model: {model})")
    print(f"{'='*60}\n")

    start = time.time()
    try:
        proc = subprocess.run(
            cmd,
            cwd=str(ROOT),
            timeout=timeout_min * 60,
            capture_output=True,
            text=True,
        )
        elapsed = time.time() - start
        if proc.returncode == 0:
            print(f"  {role} completed in {elapsed:.0f}s")
            return True, elapsed
        else:
            print(f"  {role} exited with code {proc.returncode} after {elapsed:.0f}s")
            if proc.stderr:
                # Print last few lines of stderr for diagnostics
                for line in proc.stderr.strip().splitlines()[-5:]:
                    print(f"    stderr: {line}")
            return False, elapsed
    except subprocess.TimeoutExpired:
        elapsed = time.time() - start
        print(f"  {role} timed out after {elapsed:.0f}s")
        return False, elapsed
    except FileNotFoundError:
        print("  ERROR: 'claude' CLI not found. Is it installed and on PATH?")
        return False, 0.0


