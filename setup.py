def main():
    parser = argparse.ArgumentParser(
        description="Initialize the AI Agent-Aided Research System",
    )
    parser.add_argument("--name", help="Paper name / short title")
    parser.add_argument("--journal", help="Target journal")
    parser.add_argument("--topic", help="Research topic description")
    parser.add_argument("--data", help="Available data description")
    parser.add_argument("--methods", help="Proposed methods")
    parser.add_argument("--gap", help="Research gap to address")
    parser.add_argument("--target-contribution", dest="contribution",
                        help="Intended contribution")
    parser.add_argument("--additional-context", dest="additional_context",
                        default="", help="Any extra context for agents")
    parser.add_argument("--from-file", dest="from_file",
                        help="Load seed values from a JSON file")
    parser.add_argument("--skip-agents", action="store_true",
                        help="Skip Director and Librarian agents")
    parser.add_argument("--no-start", action="store_true",
                        help="Do not auto-start watcher.py after setup")
    parser.add_argument("--overwrite", action="store_true",
                        help="Overwrite existing seed files")
    args = parser.parse_args()

    print("\n" + "=" * 60)
    print("  AI Agent-Aided Research System — Project Setup")
    print("=" * 60 + "\n")

    # --- Gather inputs -------------------------------------------------
    if args.from_file:
        path = Path(args.from_file)
        if not path.exists():
            print(f"ERROR: Seed file not found: {path}")
            sys.exit(1)
        with open(path) as f:
            seed = json.load(f)
        name = seed.get("name", "Untitled")
        journal = seed.get("journal", "")
        topic = seed.get("topic", "")
        data = seed.get("data", "")
        methods = seed.get("methods", "")
        gap = seed.get("gap", "")
        contribution = seed.get("contribution", "")
        additional_context = seed.get("additional_context", "")
    elif args.name:
        # CLI mode — all from flags
        name = args.name
        journal = args.journal or ""
        topic = args.topic or ""
        data = args.data or ""
        methods = args.methods or ""
        gap = args.gap or ""
        contribution = args.contribution or ""
        additional_context = args.additional_context or ""
    else:
        # Interactive mode
        print("Enter project details (press Enter to skip optional fields):\n")
        name = ask("Paper name / short title")
        journal = ask("Target journal")
        topic = ask("Research topic")
        data = ask("Available data")
        methods = ask("Proposed methods")
        gap = ask("Research gap")
        contribution = ask("Target contribution")
        additional_context = ask("Additional context", "")

    if not name:
        print("ERROR: Paper name is required.")
        sys.exit(1)

    date = datetime.now().strftime("%Y-%m-%d %H:%M")

    # --- Seed files ----------------------------------------------------
    print("\nSeeding project files...\n")

    seed_file(
        ROOT / "work-progress" / "plan.md",
        PLAN_TEMPLATE.format(
            date=date, name=name, journal=journal, topic=topic,
            data=data, methods=methods, gap=gap,
            contribution=contribution, additional_context=additional_context,
        ),
        overwrite=args.overwrite,
    )

    seed_file(
        ROOT / "work-progress" / "progress.md",
        PROGRESS_TEMPLATE.format(date=date),
        overwrite=args.overwrite,
    )

    seed_file(
        ROOT / "paper" / "references.bib",
        BIB_TEMPLATE,
        overwrite=args.overwrite,
    )

    seed_file(
        ROOT / "code" / "utils" / "plotting_utils.py",
        PLOTTING_UTILS,
        overwrite=args.overwrite,
    )

    seed_file(
        ROOT / "memories" / "consensus.md",
        CONSENSUS_TEMPLATE.format(name=name, journal=journal, date=date),
        overwrite=args.overwrite,
    )

    # Save seed JSON for reproducibility
    seed_data = {
        "name": name,
        "journal": journal,
        "topic": topic,
        "data": data,
        "methods": methods,
        "gap": gap,
        "contribution": contribution,
        "additional_context": additional_context,
        "setup_date": date,
    }
    seed_file(
        ROOT / "paper_seed.json",
        json.dumps(seed_data, indent=2) + "\n",
        overwrite=args.overwrite,
    )

    print("\nProject files seeded successfully.\n")

    # --- Run agents ----------------------------------------------------
    if not args.skip_agents:
        # Director agent
        director_prompt = textwrap.dedent(f"""\
            You are the Director agent. Read CLAUDE.md for your full instructions
            and .claude/agents/director.md for your detailed persona.

            The user has provided the following research seed:
            - Paper Name: {name}
            - Target Journal: {journal}
            - Topic: {topic}
            - Available Data: {data}
            - Proposed Methods: {methods}
            - Research Gap: {gap}
            - Target Contribution: {contribution}
            - Additional Context: {additional_context}

            Your task:
            1. Read work-progress/plan.md
            2. Fill in the "Director: Research Strategy" section completely
            3. Be brutally honest about publishability
            4. Suggest a concrete Experiment Contract

            Write your output directly into work-progress/plan.md.
        """)
        ok, _ = run_agent("Director", director_prompt, timeout_min=20)
        if not ok:
            print("WARNING: Director agent did not complete successfully.")
            print("You can re-run it manually or continue with setup.\n")

        # Librarian agent
        librarian_prompt = textwrap.dedent(f"""\
            You are the Librarian agent. Read CLAUDE.md for your full instructions.

            Your persona: You channel Eugene Garfield — founder of citation indexing.
            You believe that understanding a field means understanding its citation
            structure. No paper exists in isolation.

            The project:
            - Paper Name: {name}
            - Target Journal: {journal}
            - Topic: {topic}
            - Research Gap: {gap}

            Your task:
            1. Read work-progress/plan.md (especially the Director section)
            2. Search for and identify the 15-30 most relevant papers
            3. Fill in the "Librarian: Literature Review" section of plan.md
            4. Populate paper/references.bib with properly formatted BibTeX entries
            5. Create related-papers/FIGURE_QUALITY_STANDARDS.md analyzing
               competitor figure styles for this journal
            6. Be thorough — missed key references will cause rejection

            Write your output directly into the appropriate files.
        """)
        ok, _ = run_agent("Librarian", librarian_prompt, timeout_min=30)
        if not ok:
            print("WARNING: Librarian agent did not complete successfully.")
            print("You can re-run it manually or continue.\n")
    else:
        print("Skipping Director and Librarian agents (--skip-agents).\n")

